export const toSlug = (value = "") =>
  value
    .toString()
    .trim()
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, "");

export const localImageMap = localImageFiles.reduce((acc, filename) => {
  const nameWithoutExt = filename.replace(/\.[^.]+$/, "");
  const slug = toSlug(nameWithoutExt);
  acc[slug] = `/images/${filename}`;
  return acc;
}, {});